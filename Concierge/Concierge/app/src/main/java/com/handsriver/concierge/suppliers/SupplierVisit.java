public class SupplierVisit {
    private String entrySupplier;
    private String nameSupplier;
    private String gatewayId;
    private String supplierId;
    private String exitSupplier;

    public String getEntrySupplier() {
        return entrySupplier;
    }

    public void setEntrySupplier(String entrySupplier) {
        this.entrySupplier = entrySupplier;
    }

    public String getNameSupplier() {
        return nameSupplier;
    }

    public void setNameSupplier(String nameSupplier) {
        this.nameSupplier = nameSupplier;
    }

    public String getGatewayId() {
        return gatewayId;
    }

    public void setGatewayId(String gatewayId) {
        this.gatewayId = gatewayId;
    }

    public String getSupplierId() {
        return supplierId;
    }

    public void setSupplierId(String supplierId) {
        this.supplierId = supplierId;
    }

    public String getExitSupplier() {
        return exitSupplier;
    }

    public void setExitSupplier(String exitSupplier) {
        this.exitSupplier = exitSupplier;
    }
}
