public class Timekeeping {
    private String firstName;
    private String lastName;
    private String rut;
    private String entryPorter;
    private String exitPorter;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getEntryPorter() {
        return entryPorter;
    }

    public void setEntryPorter(String entryPorter) {
        this.entryPorter = entryPorter;
    }

    public String getExitPorter() {
        return exitPorter;
    }

    public void setExitPorter(String exitPorter) {
        this.exitPorter = exitPorter;
    }
}
